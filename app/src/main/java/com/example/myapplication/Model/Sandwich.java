package com.example.myapplication.Model;

public class Sandwich {
    private     String  name;
    private     String  prix;
    private     String  pain;
    private     String  url;
    private     String  provenance;

    public String getName() {
        return name;
    }

    public String getPrix() {
        return prix;
    }

    public String getPain() {
        return pain;
    }

    public String getUrl() {
        return url;
    }

    public String getProvenance() {
        return provenance;
    }
}
